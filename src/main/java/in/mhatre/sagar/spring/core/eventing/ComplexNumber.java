package in.mhatre.sagar.spring.core.eventing;

public class ComplexNumber {
	private int i,j;
	
	

	public ComplexNumber(int i, int j) {
		super();
		this.i = i;
		this.j = j;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}

	public int getJ() {
		return j;
	}

	public void setJ(int j) {
		this.j = j;
	}
	

}
